#include <stdio.h>

int ft_isalpha(int c)
{
    if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
    {
        return (1);
    }
    return (0);
}

int main(void){
    char x ='9';
        
    printf("%d", ft_isalpha(x));
    
    return 0;
}